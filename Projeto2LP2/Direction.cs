using System;

namespace Projeto2LP2
{
    /// <summary>
    /// Enum que indica a direção da cobra.
    /// </summary>
    public enum Direction
    {
        /// <summary>
        /// Para cima.
        /// </summary>
        Up,

        /// <summary>
        /// Para baixo.
        /// </summary>
        Down,

        /// <summary>
        /// Para a esquerda.
        /// </summary>
        Left,

        /// <summary>
        /// Para a direita.
        /// </summary>
        Right,

        /// <summary>
        /// Nenhuma direção definida.
        /// </summary>
        None,
    }
}
